float calculate_a_3(float I, float T) {
    return 9.66449f
         + 1.086e-04f * I
         + 0.01084f   * T
         + 4.466e-07f * I * I
         + 5.992e-05f * T * T
         + 1.621e-05f * I * T;
}

float calculate_b_3(float I, float T) {
    return 17.23203f
         + 3.348e-03f * I
         + 0.00214f   * T
         - 1.243e-06f * I * I
         + 1.605e-05f * T * T
         - 6.923e-06f * I * T;
}

float calculate_A2_3(float I, float T) {
    return 2.26089f
         - 9.692e-04f * I
         - 0.00598f   * T
         + 6.754e-07f * I * I
         + 3.766e-06f * T * T
         + 3.071e-06f * I * T;
}

float calculate_c_3(float I, float T) {
    return 29.98674f
         + 2.515e-01f * I
         - 0.29117f   * T
         - 5.071e-05f * I * I
         + 1.265e-03f * T * T
         - 7.687e-04f * I * T;
}

float calculate_x1_3(float I, float T) {
    return 535.14172f
         - 2.696e-02f * I
         + 0.05375f   * T
         + 1.647e-05f * I * I
         - 6.874e-05f * T * T
         + 3.954e-05f * I * T;
}



void update_spectrum_array_3(float I, float T, const uint16_t *X_array3, float *Y_array3, uint16_t size) {
    float a = calculate_a_3(I, T);
    float b = calculate_b_3(I, T);
   // float A1 = calculate_A1_3(I, T);
    float A2 = calculate_A2_3(I, T);
    float x1 = calculate_x1_3(I, T);
		float c  = calculate_c_3(I, T);

    for (uint16_t i = 0; i < size; i++) {
        float X = (float)X_array3[i];
        float part1 =  expf((x1 - X) / a);
        float part2 = A2 * expf((X - x1) / b);
        Y_array3[i] = c / (part1 + part2);
    }
}

